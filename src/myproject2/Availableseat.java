    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        Connection con=null;
        Statement st=null;
        ResultSet rs=null;
        String tname=comsname.getSelectedItem().toString();
        try {
            con=DbConnection.getConnect();
            st=con.createStatement();
            rs=st.executeQuery("select * from trains where t_name='"+tname+"'");
            while(rs.next()){
                txtseatno.setText(rs.getString("available"));
                txtsprice.setText(rs.getString("seat_price"));
            }
        } catch (Exception e) {
            e.printStackTrace();
            
        }
    }//GEN-LAST:event_btnsearchActionPerformed

