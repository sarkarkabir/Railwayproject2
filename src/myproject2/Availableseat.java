    public void getTrain(){
        Connection con=null;
        Statement st=null;
        ResultSet rs=null;
        String status="Available";
        try {
            con=DbConnection.getConnect();
            st=con.createStatement();
            rs=st.executeQuery("select t_name from trains where status='"+status+"'");
            while(rs.next()){
                comsname.addItem(rs.getString(1));
            }
            rs.close();
            st.close();
            con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
